def main() -> None:
    """Generate torrent files for all datasets."""
    print("Creating torrent files for datadex datasets...")
    
    # Create WDI torrent
    try:
        wdi_torrent = create_wdi_torrent()
        print(f"✓ Created WDI torrent: {wdi_torrent}")
    except FileNotFoundError as e:
        print(f"⚠ Skipping WDI torrent: {e}")
    
    # Create OWID torrent
    try:
        owid_torrent = create_owid_torrent()
        print(f"✓ Created OWID torrent: {owid_torrent}")
    except FileNotFoundError as e:
        print(f"⚠ Skipping OWID torrent: {e}")
    
    print("Torrent generation complete!")


if __name__ == "__main__":
    main()